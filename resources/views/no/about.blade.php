              Selv om det ligger stort potensial i å integrere figurer og objekter i dialog om relasjoner, er ikke dette noe som er åpenbart i seg selv.

                I 2004 gjennomført jeg en spørreundersøkelse av terapeuter som brukte Family Dialogue Set og analyserte deres svar for å få en nærmere forståelse av hvordan figurer kan brukes og hvordan de påvirker samtaler.  Jeg skrev en rapport men publisert den ikke.

                Senere, da det var fortsatt veldig lite skrevet om dette, samlet jeg informasjon fra rapporten, tilbakemelding fra familier og min egne erfaringer og kom frem til begrepet ‘relasjonsoverblikk’ som et navn for det som blir skapt når figurer tas inn i samtaler.  Og så laget jeg denne modellen, illustrert gjennom et sekskant mønstret, for å illustrere det som jeg fått frem og den ble grunnlag for dette webområde.
