                Family Dialogue Set var vellykket, men vanskelig å produsere i større skala og ganske dyrt, og produksjonen ble avsluttet i 2011.

                Jeg fikk fortsett henvendelser om figursettet og jeg utviklet et enklere men utgave basert på 2-D kort.  Det var lettere og rimeligere å produsere, men mer omfattende.

                En tidligere kollega foreslo å lage et digitalt figursett, egnet for ungdommer som var godt vant til datateknologi. Hun kjente en programmerer og jeg kjente en grafisk designer, og vi satte igang et prosjekt for å designe og bygge en app for nettbrett. En betaversjon ble utviklet for nettbrett, men plattformen det var bygget på ble utdatert og det måtte lages på nytt.

