                    <p class="small-header">BAKGRUNN</p>
                    <h2>Erfaring med figurer i samtaleterapi</h2>
                  </div>
                  <hr>
                  <p class="justi text" >

                        "Da jeg begynte å jobbe ved Familieavdelingen ved Nordlandssykehuset i 1987, slet jeg med å finne en måte å snakke med foreldre og barn sammen på. I samtaler med familier ble de yngre barna ofte enten leie og tilbaketrukne eller overaktive og "forstyrrende". Etter hvert skjønte jeg at vi kommuniserte som voksne, og brukte kun ord. Jeg så for meg et bilde av ordene som svermet over barnas hoder, vanskelige å forstå og lette å feiltolke, og jeg var bekymret for at vi økte forvirringen deres og gjorde mer skade enn nytte.

                        Jeg leste artikkelen ‘Redressing the balance - involving children in family therapy’ av Alison O’Brien og Penny Loudon i Journal of Family Therapy (JoFT 1985 v7.2 81-88). Artikkelen beskrev dilemmaet jeg sto overfor og foreslo flere måter å benytte tegninger og gjenstander på for å inkludere barn i familiesamtaler.

                        I 1988 begynte familieavdelingen tilsyn med den amerikanske familieterapeuten Harry Goolishian. Han hadde utviklet Problem Systems Approach (nå kjent som kollaborativ terapi) sammen med Harlene Anderson og kolleger ved Houston Galveston Institute.

                        Dette tilnærming innebar å ta hensyn til familiemedlemmer og andre personer som var del i å definere en "fastlåst" situasjon.

                        Så viste en kollega meg et sett med trefigurer som den norske familieterapeuten David Kvebæk hadde utviklet for å jobbe med familier.

                    <br><br><br><br>
                    <p class="small-header">BAKGRUNN</p>
                    <h2>Family Dialogue Set ble til</h2>
                    <hr>
                    <p class="justi text" >
                    Jeg innså at hvis jeg skulle bruke figurer i samsvar med denne tilnærmingen måtte jeg ha et ganske bredt utvalg, og jeg samlet sammen et sett med Playmobil-figurer.

                    Noen ganger spilte jeg brettspill med familiene på Familieenheten, og jeg fikk laget et brett som yngre barn kunne plassere figurene sine på for å gjøre kommunikasjon litt lettere for dem. Jeg brukte figurene for å legge til rette for dialog heller enn for vurdering, og jeg ville ikke ha et rutenett så brettet var basert på sekskanter som grenser likt til hverandre.

                    På Ungdomsavdelingen hadde vi brukt sosiale nettverksdiagrammer for å snakke med ungdom om deres situasjon.  Jeg fikk laget et brett med en form for nettverkskart og begynte å bruke figurene i stedet for å tegne symboler for folk. Fordi figurene var lett å flytte på, ble samtalene om de sosiale nettverkene mer fleksible og tilpasset for å se etter alternative perspektiver.

                    Så begynte jeg å bruke figurer for å sette opp genogram for å hjelpe foreldre forteller barna om sin utvidede familie.

                    Andre systemiske tilnærminger påvirket også vårt arbeid, slik som sirkulær spørring, eksternalisering av problemer og løsningsfokus, og jeg supplerte figursettet med ulike objekter, tegninger og symboler for å imøtekomme disse.
                    <br><br>
                    De ulike perspektivene til alle familiemedlemmene ble mer tydelige, og vi kom vanligvis ganske raskt frem til sentrale spørsmål rundt relasjoner slik at vi kunne snakke om dem på en konstruktiv måte. Figurene ga struktur og retning til dialogen. Ord og bilder jobbet sammen for å forbedre kommunikasjonen og engasjerte hele familien. Elementer av lekenhet og humor var ofte til stede selv i diskusjon av vanskelige spørsmål, og dette hadde en tendens til å løsne rigide posisjoner og fremme mer konstruktive holdninger til spørsmål om hvordan man kunne gå videre.
                    Etter et par års bruk av figursettet mitt innså jeg at det også ville være til nytte for andre familieterapeuter, og jeg bestemte meg for å produsere det for salg. Playmobil-figurene limte jeg på runde baser og plasserte i trebrett for utstilling. Jeg inkluderte «magiske figurer», kjæledyr, penner, tankebobler og plastelina og la alt i en koffert slik at det enkelt kunne transporteres. Jeg skrev brukerveiledninger og kalte det Family Dialogue Set for å fremheve det som et samtaleverktøy, heller enn en test. Den ble produsert mellom 1991 og 2011, og den ble kjøpt opp av barne- og familieavdelinger for psykisk helse, barnevernsbyråer og pedagogiske støttetjenester i Norge, Sverige og Danmark.
