              People in my Life er et variert og omfattende sett med figurer i to utgaver: som figursett og som digital applikasjon for nettbrett og datamaskiner. De ble utviklet av Steven Balmbra for å gjøre det mulig for rådgivere og terapeuter å introdusere samtaler med relasjonsoverblikk i sin praksis.
                <br>

                Figursettet People in my Life består av over 300 dobbeltsidig kortfigurer, to utstillingsesker og flere grunnmønstre for å hjelpe med plasseringen av figurene. Antallet og variasjonen av figurer gjør det mulig å lage store relasjonsoverblikk som inkluderer en persons familie, venner, naboer, kolleger og bekjente.
                <br>

                Den digitale appen People in my Life er godt egnet til å skape relasjonsoverblikk med barn og unge som er velkjent med å forholde seg til visuelle medier. Appen er omtalt, sammen med Play of Life, i artikkelen 'Show me! Using digital figures to facilitate conversations in systemic therapy' av Steven Balmbra og Carlos A Raimundo, publisert i april 2021 i Journal of Family Therapy Vol. 43 / 2

