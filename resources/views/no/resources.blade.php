              Virginia Satir var pioneer for familieskulptur-metoden, dannet av og med mennesker i terapirommet. Den norske familieterapeuten David Kvebæk begynte å bruke figurer til å lage familieskulpturer som en måte å forklare familiedynamikk til sine kolleger, men tok dem raskt i bruk som en terapimetode. Kvebaek Family Sculpture Technique er et sett med trefigurer som brukes til å danne familieskulpturer.

               <br><br>

               Kvebæks arbeid vokste fra et objektrelasjons perspektiv, og figur-oppstillingene ble betraktet som projeksjoner av menneskers indre sosiale virkelighet med potensial til å fremme gjensidig innsikt blant familiemedlemmer. Hans grunnleggende spørsmål "Hvordan ser du på ting nå?" "Hvordan var ting før?" og "Hvordan ser du på ting i fremtiden?" er grunnleggende for samtaler om relasjonsoverblikk.

               <br> <br>

               Julie Thorsheim i USA har fulgt opp David Kvebæks arbeid. Nettstedet hennes Kvebaek Sculpting har omfattende informasjon om tilnærmingen og Kvebaek-figursettet kan bestilles der.

