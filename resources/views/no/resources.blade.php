                    Det finnes få spesiallagde sett med figurer for å snakke om relasjoner. Det er strengt tatt heller ikke nødvendig å bruke figurer. Jim Wilson skrev om å bruke gjenstander som kongler, steiner, penner og blokker for å lage 'små skulpturer' i sin bok Child-Focused Practice (Karnac, London). Sjakkbrikker kan også brukes, og kan enkelt males i forskjellige farger for å skille dem fra hverandre. Abstrakte figurer eller objekter passer fint til mindre relasjonsoverblikker, men ting kan lett bli forvirrende hvis de brukes til å vise større sosiale nettverk.

