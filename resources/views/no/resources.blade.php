              Nettverkskart har vanligvis blitt tegnet med symboler for kvinner, menn osv. Dette arbeidet ble utviklet av Urie Bronfenbrenner som publiserte et 5-sektors sirkulært diagram. Hver sektor representerer et område av sosiale relasjoner - familie, slektninger, venner, arbeid/skole, nabolag, profesjonelle hjelpere, og avstand fra senteret som indikerer graden av mellommenneskelig/emosjonell nærhet eller avstand. Det finnes nå en rekke former for nettverkskart og omfattende litteratur om å jobbe med dem. Nettverkskart kan ha stor nytte av å lages ved hjelp av figurer ettersom de blir bevegelige og fleksible i bruk.

