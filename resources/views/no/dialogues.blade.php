                    En scene kan også spilles av på nytt fra en annen persons synspunkt.
                    I 'Playback theatre'-metoden legges det til en 'reparert' versjon av en situasjon, der noen eller noe griper inn og hindrer det i å slå dårlig ut.
                    Dette kan bidra til å ta litt smerte ut av et dårlig minne og redusere enhver urimelig skyldfølelse og selvbebreidelse.
                    Hvis det har vært problemer for barn på skolen, kan de lage en konfigurasjon av elevene og lærerne som var involvert for å snakke om avvisning, hån, trusler og mobbing i klasserommet, lekeplassen eller på vei til skolen.
