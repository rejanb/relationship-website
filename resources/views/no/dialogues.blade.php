                Dialogen kan dreie seg om sannsynligheten for hvert utfall og hvilke hendelser som kan føre til de ulike utfallene.
                Hva må til for å unngå at ting blir dårlig? Hva ville konsekvensene være for andre?

                <br><br>


              <strong>Ambivalens  –</strong>
                Hvordan ting ser ut fra ulike ståsteder.
                Å sammenligne hvordan en person ser ting fra en positiv, optimistisk holdning og fra en negativ, pessimistisk holdning.
                Dialogen kan dreie seg om hvordan man kan utvikle og opprettholde en hjelpsom holdning, hvem som kan støtte den og hva og hvem som vil undergrave den.
