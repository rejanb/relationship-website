            <strong>Eksternalisering av problemer</strong>
                  <br>

                    Michael White og David Epston utviklet den narrative metoden for eksternalisering av problemer.
                    Et eksternalisert problem kan representeres på en eller annen måte - med en figur, en gjenstand, tegnet på et kort, modellert i plastelina.
                    Figurer kan brukes til å sette opp og spille ut en scene, typisk for den dominerende fortellingen der det eksternaliserte problemet skaper vanskeligheter.
                    Dialog kan bevege seg til situasjoner som gikk bedre, hvem eller hva som ville gjort en forskjell og hvordan man bruker denne informasjonen til å hjelpe til med å temme problemet og gjenvinne kontrollen over ens liv.


                    <br> <br>



            <strong>Løsningsfokus</strong>

                En scene kan settes opp med figurer der en spesiell vanskelighet ikke lenger er tilstede og personen eller familien kan snakke om hvordan livet er nå, hvordan forholdene har endret seg og hva som angår menneskene nå.
                Dialogen kan spore tilbake til hvordan de klarte å komme frem til denne situasjonen. Når folk setter ut figurer, vil de utforske og demonstrere sitt eget perspektiv.
                Det er en dualitet i utsagn og spørsmål, 'Slik ser jeg det' og 'Er det slik jeg ser det?


            <br> <br>


            <strong>Rollereversering med figurer</strong>

                Rollereversering er en teknikk hentet fra psykodrama gruppeterapi, hvor noen veiledes til å sette seg selv i en annens posisjon.
                De kan begynne med å holde sin egen figur og stille et spørsmål eller si noe til den andre personen. De legger fra seg figuren og tar figuren til den andre personen, fokuserer tankene på hvordan de vil svare, og sier et svar høyt. De bytter til sin egen figur og sier sitt eget svar. En dialog utvikles gjennom videre utveksling av figurer og svar. De er i stand til å uttrykke ting som ikke er sagt, samtidig som et glimt av den andres perspektiv har en tendens til å komme frem.
                Rollereversering er en kraftig teknikk som krever erfaring og trening.


