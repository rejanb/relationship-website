                <h5> <strong>Peopling</strong></h5>

                <p style="margin-bottom:0px"  class="text">
                These are dialogues arising from choosing figures, perceiving them as representing
                people, and then placing them to form a relationship overview.
                When someone has chosen a figure for themself they can at once be asked about how they see themselves.
                When they choose figures for other people they can talk about how they see them, get on with them and how they get on with each other.
                Having a figure to hold can help reflection over ‘what that person means to me and what I mean to them’. Pets and other significant animals can also be included.
                When figures are chosen to represent people, they facilitate the telling of stories.
                Once figures are placed on a table you can ask about where they are standing in relation to each other, how the people they represent get on with each other, what they do together, and so on.
                Where and how they are arranged tends to invite curiosity and it is a relatively small step to consider the point of view of another person.
                </p>

