            <p  class="text" >

              <br>
              <strong>Spectogram</strong>
              <br>
                The spectogram is a sociometric group exercise devised by J.L.Moreno as a way of researching social groups.
                Spectrograms are based on simple scales going from ‘Very Little’ to ‘Very Much’ where people place themselves according to their view or evaluation of a particular issue.
                Figures can be placed on a line on the table to illustrate how a person sees different people’s evaluation of particular themes and the dialogue can centre on various aspects of these evaluations.
                It can be used for showing and discussing something as simple as people's ages, or it might be their levels of feeling safe, certain, of a preference, an attitude or so on.
                The dialogue can continue about the reasons people stand where they are, the implications of their positions, the likelihood of change and so on.

              <br> <br>
             <strong>Emotional awareness</strong>
              <br>
                In a conversation addressing a person’s emotional awareness or experience, a drawn circle can be divided into segments, each labelled to an emotion.
                A person can then place a figure to show how intensely it was felt in a particular situation.
                This process can help in sorting out mixed and confused feelings, and differentiating between thoughts and feelings.
                There are several theories about which are a person’s basic emotions, with numbers ranging from six to over thirty.
                The most usual include degrees of Anger, Sadness, Happiness, Fear, Excitement, Tenderness, Disgust, Trust, Surprise, Vigilance and Contempt.


            <br> <br>
             <strong>Making a decision together</strong>
              <br>
              The segments of the coloured circle can be labelled to represent the various options in decision making.
                Family or group members can place figures to show each other their preferences and discuss the nuances of their reasons when they are making a decision together or understanding how and why a decision was made.


            </p>


