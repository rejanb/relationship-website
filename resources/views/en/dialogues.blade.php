                <h5> <strong>Surveying</strong></h5>

                <p style="margin-bottom:0px"  class="text">
                This is about considering the wider context and gaining fresh perspectives of how things could be different.
                In times of difficulty, people can easily over-focus on the day-to-day tribulations of their situation and lose sight of the larger picture.
                You might say that they cannot see the forest for all the trees and need help to elevate their vantage point.
                Surveying gives an impression of the configuration as a whole.  It invites dialogue about contextual aspects of their relationships and how the various parts are contributing to the whole.
                It invites reflection over the question “Who is in my life and how are they there?” and it can help identify resources that have been overlooked.
                </p>

