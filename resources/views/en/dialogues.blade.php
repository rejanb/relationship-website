              <div class="collapse" id="COMC">
            <p  class="text" >
              <br>
              Here are some of the kinds of comparison that can be made through different arrangements of a relationship overview:
              <br><br>
            <strong>Time - </strong>
                How things look now and how they looked then.
                This could also be before and after a particular event or situation.
                The dialogue can be about what are the differences, how they have come about, the implications they have had, what could have been different, what they have learnt from the change.

              <br><br>
              <strong>Hopes and fears – </strong>
                    How I hope they will be and how I am worried they could turn out.
                    Comparing how a person hopes things will turn out with how they are worried they may become.
                    The dialogue can concern the likelihood of each outcome and what turns of events could lead to the different outcomes.
                    What would be necessary to avoid things turning out badly? What would the implications be for others?

                <br><br>
              <strong>Ambivalence –</strong>
                 How things appear from different attitudes.
                Comparing how a person sees things from a positive, optimistic attitude and from a negative, pessimistic attitude.
                The dialogue could turn to how to develop and maintain a helpful attitude, who can support it and what and who would undermine it.
                This could also be from other contrasting perspectives such as ‘from my rational side’ and ‘from my emotional side’.
                <br><br>
              <strong>Someone else’s perspective - </strong>
                 How it looks for me and how it looks for you.
                Rearranging the relationship overview to show how a person believes that another person sees the situation (it could even be how the family dog or the fly on the wall would view things).
                Do you think other people would see this in the same way as you? Who would see things differently? How would they see it?
                <br><br>
              <strong>Family perspectives – </strong>

                In family sessions, each family member’s perspective can be compared to others.
                After a family member has set out the figures you can ask other family members to show whether they have understood their situation in a similar way or differently.
                For example, you can ask a parent to keep their child’s figure in place but move other figures to demonstrate how they have understood their child’s situation.
                Then you can talk about the differences and what they have meant.
                This can provide better insight into how they understand each other, how well they mentalise each other.

                <br><br>
                <strong>Alternative realities  – </strong>
                How it would look if the opposite was true, or if the impossible was possible.
            </p>


