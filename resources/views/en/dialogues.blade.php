                    <h2>Comparing</h2>
                    <hr>
                  </div>
                  <p style="margin-bottom:0px"  class="text">
                     Relationship overviews are well suited for comparing and contrasting different situations and points of view.
                    Figures in a configuration are easily moved, and can show alternative perspectives that can be compared.
                    Making visual comparisons is a very effective way of facilitating dialog about agreement and disagreements.
                    They can give people a picture of another person’s point of view and bring fresh insight and renewed understanding.
                    They can bring some flexibility to stuck attitudes and rigidly held opinions.
                    Various perspectives can be configured, compared and the possibility for change can be explored in dialogue
                        </p>

                </div>
              </div>

              <div class="col-12 col-md-3">
