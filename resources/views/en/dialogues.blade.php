                <p style="margin-bottom:0px"  class="text">
                Figures from relationship overviews can be used for illustrating and playing out events, situations or incidents significant to a person, their relationships and self-image.
                 <br> <br>
                  <strong>Role play with figures</strong>
                  <br>
                    The figures can be used to demonstrate events and situations that have been important to the person, what they remember and how they experienced them.
                    They can be used to explore particular situations or incidents that are or have been significant to the person, their relationships and self-image through playing out how a person remembers the situation.
                    This would involve setting out the figures of the people involved to make a small scene on the table.
                    Small objects can be added to help set the scene. The situation can then be played out as a figure theatre to show how the person remembers it.
                    </p>

