            <div class="collapse" id="PLAC">
          <p  class="text" >
            <br>
            A scene can also be played out again from another person’s point of view.
            In the ‘Playback theatre’ method a ‘repaired’ version of a situation is added, where someone or something intervenes and prevents it from turning out badly.
            This can help to take some pain out of a bad memory and reduce any unreasonable guilt and self-blame.
            If there have been issues for children at school, they can make a configuration of the pupils and teachers that were involved to talk about rejection, mocking, threatening and bullying in the classroom, playground or on the way to school.
            These stories are often hidden from the parents and teachers even when they have a good general understanding of the school environment.
            <br><br>



            <strong>Externalising problems</strong>
                  <br>
                  Michael White and David Epston developed the narrative method of externalising problems.
                    An externalised problem can be represented in some way - by a figure, an article, drawn on a card, modelled in plasticine.
                    Figures can be used to set up and play out a scene typical for the dominant narrative where the externalised problem is causing difficulty.
                    Dialogue can move to situations that turned out better, who or what would make a difference and how to use this information to help tame the problem and regain control of one’s life.
                    <br> <br>



            <strong>Solution focus</strong>
            A scene can be set up with figures where a particular difficulty is no longer present and the person or family can talk about what life is like now, how relationships have changed and what is concerning the people now.
            The dialogue can trace back to how they managed to arrive at this situation. Whenever people set out figures they will be exploring as well as demonstrating their own perspective.
            There is a duality of statement and question ‘This is how I see it’ and ‘Is this how I see it?
            <br> <br>


            <strong>Role reversal with figures</strong>
            Role-reversal is a technique taken from psychodrama group therapy, where someone is guided into putting themselves in the position of another.
            They can begin by holding their own figure and asking a question or saying something to the other person.  They put down their figure and take the figure of the other person, focus their thoughts on how they would answer, and say a reply out loud. They swap to their own figure and say their own response. A dialogue develops through the continued swapping of figures and responses.  They are able to express things that have not been said at the same time as a glimpse of the other person’s perspective tends to come out.
            Role reversal is a powerful technique that requires experience and training.

            <br> <br>

          </p>
