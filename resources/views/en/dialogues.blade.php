                  <p style="margin-bottom:0px"  class="text">
                  A relationship overview can become an arena for exploring a person's social  situation.
                  <br>
                    Here are a few examples:
                    <br>
                    <br>
                    <strong>Genogram</strong>
                    <br>
                    Constructing a genogram and talking about the various aspects of the culture of a family is a very fruitful way of working on the understanding of alliances, and conflicts.
                    When a couple joins together, each brings with them a wealth of experiences, attitudes, loyalties, habits, understandings, duties, and narratives to the new relationship and they have a task of bringing these together, adding something new and forming their own way of living.
                    It can be very valuable to explore these various influences when helping them to manage areas of disagreement and conflict.


                   </p>

