                <h2>Putting Together a Collection of Figures</h2>
                <hr>
              </div>
              <p class="text">


                     There are few purpose-made sets of figures for talking about relationships. It is not strictly necessary to use figures.
                    Jim Wilson wrote about using objects such as conkers, stones, pens and blocks to make 'mini-sculptures' in his book Child-Focused Practice (Karnac, London).
                    Chess pieces can also be used, and can easily be painted different colours to distinguish them from each other.
                    Abstract figures or objects are fine for forming small relationship overviews, but things can easily become confusing if they are used to show social networks.


                </p>
                <p class="text">


                Another way is to gather a collection of new or used children's toy figures.
                I prefer to use fairly unadorned figures to keep the conversations on a realistic level.
                Lego and Playmobil figures come in many varieties and can easily be collected.

                </p>
