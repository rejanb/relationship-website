                Although there is great potential in integrating figures and objects in dialogue about relationships, this is not something that is obvious in itself.

                In 2004, I conducted a survey of therapists who used the Family Dialogue Set and analyzed their responses to gain a closer understanding of how figures can be used and how they influence conversations.  I wrote a report but did not publish it.

                Later, when there was still very little written about this, I gathered information from the report, feedback from families and my own experiences and came up with the term 'relational overview' as a name for what is created when figures are included in conversations.  And then I made this model, illustrated through a hexagon pattern, to illustrate what I got and it became the basis for this website.

