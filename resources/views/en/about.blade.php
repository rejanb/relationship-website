                <div class="heading">
                    <p class="small-header">BACKGROUND</p>
                    <h2>Experience with figures in talk therapy</h2>
                  </div>
                  <hr>
                  <p class="justi text" >

                  "When I started working at the Family Department at Nordlandssykehuset in 1987, I struggled to find a way to talk to parents and children together. In conversations with families, the younger children often became either sullen and withdrawn or overactive and "disturbing". Eventually I realized that we communicated like adults, using only words. I imagined the words swarming over the children's heads, difficult to understand and easy to misinterpret, and I worried that we were adding to their confusion and doing more harm than good.

                    I read the article 'Redressing the balance - involving children in family therapy' by Alison O'Brien and Penny Loudon in the Journal of Family Therapy (JoFT 1985 v7.2 81-88). The article described the dilemma I was facing and suggested several ways to use drawings and objects to include children in family conversations.

                    In 1988, the family department began supervising American family therapist Harry Goolishian. He had developed the Problem Systems Approach (now known as collaborative therapy) with Harlene Anderson and colleagues at the Houston Galveston Institute.

                    This approach meant taking into account family members and other people who were part of defining a "stuck" situation.

                    Then a colleague showed me a set of wooden figures that the Norwegian family therapist David Kvebæk had developed for working with families.

                    <br><br><br><br>
                    <p class="small-header">BACKGROUND</p>
                    <h2>  The Family Dialogue Set was born</h2>
                    <hr>


                    I realized that if I was going to use figures in accordance with this approach I would have to have quite a wide variety and I assembled a set of Playmobil figures.

                    Sometimes I played board games with the families at the Family Unit, and I had a board made for younger children to place their pieces on to make communication a little easier for them. I used the shapes to facilitate dialogue rather than assessment, and I didn't want a grid so the board was based on hexagons that bordered each other equally.

                    At the Youth Department, we had used social network diagrams to talk to young people about their situation.  I had a board made with some kind of network map and started using the figures instead of drawing symbols for people. Because the figures were easy to move around, the conversations about the social networks became more flexible and adapted to look for alternative perspectives.

                    Then I started using figures to set up genograms to help parents tell their children about their extended family.

                    Other systemic approaches also influenced our work, such as circular questioning, problem externalization and solution focus, and I supplemented the figure set with various objects, drawings and symbols to accommodate these.

                    <br><br>
                    The different perspectives of all the family members became clearer, and we usually arrived at key relationship issues quite quickly so that we could talk about them in a constructive way. The figures gave structure and direction to the dialogue. Words and pictures worked together to improve communication and engage the whole family. Elements of playfulness and humor were often present even in discussion of difficult issues, and this tended to loosen rigid positions and promote more constructive attitudes to questions of how to move forward.
                    After a couple of years of using my figure set, I realized that it would also benefit other family therapists, and I decided to produce it for sale. I glued the Playmobil figures onto round bases and placed them in wooden trays for display. I included "magic figures", pets, pens, thought bubbles and plasticine and put everything in a case for easy transport. I wrote user guides and called it the Family Dialogue Set to emphasize it as a conversation tool, rather than a test. Produced between 1991 and 2011, it was acquired by child and family mental health departments, child welfare agencies and educational support services in Norway, Sweden and Denmark.

                </p>

