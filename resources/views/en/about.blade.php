                I continued to receive inquiries about the figure set and I developed a simpler but edition based on 2-D cards. It was easier and cheaper to produce, but more extensive.

                A former colleague suggested creating a digital figure set, suitable for young people who were well used to computer technology. She knew a programmer and I knew a graphic designer, and we started a project to design and build an app for tablets. A beta version was developed for tablets, but the platform it was built on became outdated and it had to be remade.

