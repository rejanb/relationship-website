                    A relationship overview enhances a conversation with a unique visual and spatial communication.
                    Seeing a relationship overview can help to clarify and simplify situations that a person has experienced as confusing and chaotic.
                    It can help a family make more sense out of a dilemma they are facing.

                    <br>

                    Figure arrangements provide spatial overviews of social relationships which provide additional perspective for considering their situation and understanding each other.
                    The communication is direct and the visual information it brings gives increased clarity to the verbal conversation.

                  <br>

                    Because visual and spatial expressions of this kind are unusual, particularly for adults, they provide fresh information about a situation.
                    How the figures are placed invites curiosity about the point of view of the person represented, and creates interest in hearing more about what they are expressing and why.
                    When talking with groups and families, people show each other an expression of how they view their relationships, and this will support a dialogical form of conversation.


