                Looking at a relationship overview gives fresh information on perceptions of relationships.
                With a couple, family, or group, one person will often gain a direct insight into another’s perceptions and the conversation about this will enhance their understanding.
                It is quite usual for parents to express surprise at the way their children understand things as they often overlook how differently they process information.
                An arrangement of figures is an unusual expression of a person’s social reality.


                <br>


                It has an element of revelation for the person setting it out as well as anybody observing.
                This is because they probably have not done this before and have no idea what it will look like until they have formed it.
                As they are in a surveying position, they will have plenty of opportunity to reflect over the sight of it.
                Because the various relationships are shown in context with each other there is plenty of new information to take stock of.

