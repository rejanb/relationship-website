                <h5> <strong>Focus and Structure</strong> </h5>
                <p   data-aos="fade-right"  data-aos-duration="1700"  class="text">

                    Using figures provides a conversation with a visual point of focus.
                    The person setting out the relationship overview devotes a lot of their attention to the figures and to the arrangement, less to the others in the room.
                    The attention of others is directed both at the person setting out the figures and at arrangement.
                    This division of attention can relieve pressure on the person talking, as they are not the only centre of attention.
                    Several therapists I have interviewed pointed out that using figures gives a conversation a clearer structure.
                    Having something visual and concrete to talk about helps children to talk about themselves and their situations more easily


                    <br>


                    It also helped to get the conversation going, gave direction to the therapist’s questions and led quickly to issues that were important to talk about.
                    "When the relationship overview helped to get the conversation going and led quickly onto important issues, it was not the position of the figures that was of primary importance, it was the dialogue concerning the placements that was of therapeutic value. Picking up on the relevance of how the figures are placed and using this to explore the various understandings and possibilities makes using them therapeutic."


                    <br>

                    A relationship overview is a simple, ordered expression of relationships, and this structure can help to clarify and simplify a situation that the person has experienced as chaotic.
                    This clarification can free perspectives that have become rigid and stuck.
                    It may challenge the therapist, who also has to divide their attention between the overview, the person talking and the others in the room.

                </p>
