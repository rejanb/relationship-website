                <h5> <strong>Variety: Mobility and Flexibility</strong> </h5>
                <p   data-aos="fade-left"  data-aos-duration="1700"  class="text" >

                Figures can easily be moved, rearranged to compare and contrast different perceptions which makes genograms and network maps flexible, a big advantage over drawing these.
                    <br>

                    Mobility is an essential element of dialogue as there is a continuing to-and-fro, from one to the other, which requires a positive energy and engagement.
                    There is an expectation that differences of perspective will be brought up and aired respectfully
                    <br>

                    Talking about a difficulty in the same way in which it was formed will be unlikely to create movement, so present understandings have to be challenged and alternative perspectives brought to the fore.
                    In a dialogue there is a potential for development for all participants when a situation is viewed in a new way that is both acceptable to them and that they can act upon.

                  <br>

                    A single arrangement of figures is quite static and there is a risk that the arrangement can be understood as a true representation of a person’s reality.
                    This can contribute to leaving the family more stuck in their thinking if a therapist stops with just one view of a situation.


