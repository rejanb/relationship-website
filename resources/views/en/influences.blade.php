                            <h5> <strong>Betweenness</strong> </h5>

                            <p class="text" data-aos="fade-left"  data-aos-duration="1700"   >

                            A relationship overview is literally between the people involved in the conversation.
                            This enhances an understanding that difficulties in relationships are best seen as between the people involved, rather than within them.
                            Generally speaking, there is more room for flexibility in finding ways of resolving conflicts when considering them as between people rather than as a result of the nature of individuals.
                            In a social network there are often people with a greater degree of connectedness and influence than others.
                            This is related to betweenness as a mathematical concept, which describes the centrality of nodes in a matrix.


