                <h5> <strong>An Elevated Perspective</strong> </h5>
                <p   data-aos="fade-right"  data-aos-duration="1700" class="text">

                People look at a relationship overview from a position a short distance away and slightly above.
                It is a kind of bird's eye view and a colleague of mine referred to this as being in an elevated position that combines observation and participation.
                This is the visual and emotional distance of surveying from the above, together with the personal, emotional intensity of knowing that this is about my life and the people I know and care about.
                The well-known expression ‘Not being able to see the wood for the trees’ has relevance here, as a relationship overview can elevate a person’s gaze from being within the ‘trees’ so that they see a representation of their ‘social woodland'.
                It gives them the impression of looking onto their situation, at the same time as they retain their subjective experiences - a duality of perspective.

                <br>

                This surveying position, a kind of participant observation in the conversation, can sharpen awareness of the content, scope and connections of the various aspects of the relationships.
                A surveying position allows talking about the contents of a person’s relationships as they are drawn to see and reflect over the thoughts and opinions they have concerning their situation.
                Questions arise about which people belong together and which do not, and how they are involved.They can be invited to consider how the situation would look from another point of view.
                The person arranging figures will be exploring their own understandings as well as demonstrating their perspectives.


