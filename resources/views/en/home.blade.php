                Relationship overviews can vary in form and size, and be used in many kinds of consultations in a variety of settings with individuals, families, and groups.
                They can be very small - a couple or a family, or large, showing a whole social network. Smaller relationship overviews can be easily made with any kind of objects - chess pieces, buttons, stones, etc. However, a collection of figures is much better suited for representing larger social contexts and will open for a greater range of uses and approaches.
                Figures and objects can be set out and arranged to show the people who are important to a person and how they influence their life. As this is an unusual form of expression for most people it is capable of bringing out new perspectives to the dilemmas and difficulties that they are facing.
                Relationship overviews stimulate fresh ideas and are effective in bringing out stories about the ways people get on together and how various relationships are influencing each other, seen in a broader social context.
                Making and discussing a relationship overview is an active and creative process that easily engages people of all ages.

                <br><br>

                The configurations of figures and objects captures and focuses attention and usually brings an element of playfulness to conversations that can free people's thinking from their preconceptions.
                People ‘listen’ with both their ears and eyes while they reconsider their own understandings and those of other people who are important to them.
                Because figures and objects can easily be moved around, relationship overviews are very flexible aids to dialogue.
                The pages of this website present various kinds of relationship overview.  They look into the kinds of dialogue they can lead to and the influences they have on a conversation.
