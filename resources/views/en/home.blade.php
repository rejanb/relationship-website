              <p class="text">

                A relationship overview is formed when someone sets out a configuration of figures or objects to represent the people who are influencing their life.
                This can be very helpful in professional consultations with people who are experiencing difficulties in their relationships as the figure configurations easily lead to constructive dialogues about dilemmas, difficulties, understandings and resources.
                Relationship overviews can be used in all sorts of conversations: with children and adults, individuals, couples, families and groups. They can be a helpful aid to professionals in many different settings where issues around relationships need to be talked about.
                This may be the therapy room, consultation office, hospital ward, care centre, school room, and workplace.

